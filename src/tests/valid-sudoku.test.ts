import { validSudoku } from '../problems/valid-sudoku'

describe('valid-sudoku', () => {
  test('case 1', () => {
    const board = [
      ['1', '2', '.', '.', '3', '.', '.', '.', '.'],
      ['4', '.', '.', '5', '.', '.', '.', '.', '.'],
      ['.', '9', '1', '.', '.', '.', '.', '.', '3'],
      ['5', '.', '.', '.', '6', '.', '.', '.', '4'],
      ['.', '.', '.', '8', '.', '3', '.', '.', '5'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '.', '.', '.', '.', '.', '2', '.', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '8'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9']
    ]
    expect(validSudoku(board)).toBe(false)
  })

  test('case 2', () => {
    const board = [
      ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9']
    ]
    expect(validSudoku(board)).toBe(true)
  })

  test('case 3', () => {
    const board = [
      ['8', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9']
    ]
    expect(validSudoku(board)).toBe(false)
  })
})
